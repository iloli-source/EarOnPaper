"""採譜物の権利ガイダンス(F-073)。

自動採譜した楽譜は「あなたが作った成果物」だが、**原曲の著作権はそれとは別に存在する**。
非音楽家ユーザーがうっかり権利侵害しないよう、配布/販売/公開の前に確認すべき点を
教育的に示す。法的助言ではない(免責)。UI/CLI から `rights` で表示する。

このテキストは事実ベースの一般的注意喚起であり、特定の楽曲・国の法制度を判断しない。
"""

from __future__ import annotations

# 一般的注意喚起(日本の著作権法を主眼にした平易な説明)。法的助言ではない。
_RIGHTS_LINES = [
    "■ 採譜物の権利について(配布・販売・公開の前に)",
    "",
    "自動採譜した楽譜データは、あなたの操作で生成された成果物です。ただし、",
    "元になった楽曲(メロディ・和声・歌詞)の著作権は、採譜しても消えません。",
    "",
    "・私的複製(個人・家庭内など限られた範囲での利用)は通常問題ありません。",
    "・第三者への配布/販売/インターネット公開/演奏には、原曲の権利者",
    "  (作詞・作曲・編曲・音楽出版社)の許諾が必要になる場合があります。",
    "・採譜(編曲・翻案)そのものにも原権利者の権利が及ぶことがあります。",
    "・著作権が消滅した楽曲(パブリックドメイン)は自由に扱えます。保護期間は",
    "  国や作者の没年で異なるため、個別に確認してください。",
    "・日本国内の演奏・配信の権利処理は JASRAC/NexTone 等の管理団体が窓口です。",
    "",
    "※ これは一般的な注意喚起であり、法的助言ではありません。商用利用や",
    "   配布を予定する場合は、権利者または専門家に確認することをおすすめします。",
]


def rights_notice() -> str:
    """権利ガイダンスの全文(複数行テキスト)を返す。"""
    return "\n".join(_RIGHTS_LINES)


def rights_summary() -> str:
    """1行の要約(採譜結果JSON等に添えるための短い注意)。"""
    return (
        "採譜物の配布/販売/公開には原曲の著作権処理が必要な場合があります"
        "(`rights` サブコマンドで詳細)。私的複製は通常問題ありません。"
    )
